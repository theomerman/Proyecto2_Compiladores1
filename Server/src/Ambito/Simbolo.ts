export class Simbolo{
    id : any;
    valor : any;
    tipo : any;
    linea : any;
    columna : any;

    constructor(_id: any, _valor: any, _tipo: any, _linea: any, _columna: any){
        this.id = _id;
        this.valor = _valor;
        this.tipo = _tipo;
        this.linea = _linea;
        this.columna = _columna;
    }
}
